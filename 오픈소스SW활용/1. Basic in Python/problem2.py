"""
What are the values of the following expressions, assuming that s ="Hello" and t = "World"?
"""

s = " Hello"
t = "World"

print('len(s)+len(t) :', len(s) + len(t))
print('s[1]+s[2] :', s[1] + s[2])
print('s[len(s)//2] :', s[len(s) // 2])
print('s+t :', s + t)
print('t+s :', t + s)
print('s*2 :', s * 2)